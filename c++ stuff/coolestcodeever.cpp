#include <iostream>
#include <cmath>
 void rendercube(int height, int length, int xpos)
 {

    for (int i = 0;height = 0; ++i)
    {
        for (int i;length = i; i++)
        {
            for (int i = 0; i = xpos; i++)
            {
                std::cout << " ";
            }
            std::cout << "#";
        }
    }
    
 }
 
 int main(){
    int height = 5;
    int length = 3;
    int xpos = 0;
    rendercube(height, length, xpos);



    return 0;
 }
